package Modelos;

public class Departamentos {
    int identificadorDepartamento;
    String nombreDepartamento;
    String descripcionDepartamento;

    public int getidentificadorDepartamento() {
        return identificadorDepartamento;
    }

    public void setIdentificadorDepartamento(int identificadorDepartamento) {
        this.identificadorDepartamento = identificadorDepartamento;
    }

    public String getnombreDepartamento() {
        return nombreDepartamento;
    }

    public void setNombreDepartamento(String nombreDepartamento) {
        this.nombreDepartamento = nombreDepartamento;
    }

    public String getdescripcionDepartamento() {
        return descripcionDepartamento;
    }

    public void setDescripcionDepartamento(String descripcionDepartamento) {
        this.descripcionDepartamento = descripcionDepartamento;
    }
}
